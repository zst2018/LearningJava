class ConstantDemo{
	public static void main(String[] args){
		//字符串常量的输出
		System.out.println("nihao");
		//整数常量的输出
		System.out.println(230);
		//小数常量的输出
		System.out.println(12.98);
		//字符常量的输出
		//System.out.println('ni');(有问题)
		System.out.println('a');
		//布尔常量的输出
		System.out.println(true);
		
	}
}